export const POKEMON_MAP: Record<string, PokemonEntry> = POKEMON_LIST.reduce(
    (acc, p) => {
        acc[p.name] = p;
        return acc;
    },
    {} as Record<string, PokemonEntry>
);

export function searchPokemon(query: string, limit: number = 10): PokemonEntry[] {
    const q = query.trim().toLowerCase();
    if (!q) return POKEMON_LIST.slice(0, limit);
    return POKEMON_LIST.filter((p) => p.name.includes(q)).slice(0, limit);
}

export function dexToImageUrl(dex: number): string {
    const padded = dex.toString().padStart(3, "0");
    return `https://www.pokemon.com/static-assets/content-assets/cms2/img/pokedex/detail/${padded}.png`;
}

export function dexToPokemonName(dex: number): string {
    const entry = POKEMON_LIST.find((p) => p.dex === dex);
    return entry ? entry.name : "";
}

export function getRandomPokemon(): PokemonEntry {
    const index = Math.floor(Math.random() * POKEMON_LIST.length);
    return POKEMON_LIST[index];
}

export function getRandomPokemonDex(): number {
    return getRandomPokemon().dex;
}
